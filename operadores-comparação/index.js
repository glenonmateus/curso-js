/*
 * Operadores de compração
 * > maior que
 * >= maior que ou igual a
 * < menor que
 * <= menor que ou igual a
 * == igualdade
 * === igualdade estrita (valor e tipo)
 * != diferente
 * !== diferente estrito (valor e tipo)
 */
console.log(10 > 5);
console.log(10 >= 5);
console.log(10 >= 11);
console.log(10 == "10");
console.log(10 === "10");
