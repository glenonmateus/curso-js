/*
 * Incremento
 * operador ++
 * Decremento
 * operador --
 */

let contador = 1;
contador++;
contador++;
++contador;
console.log(contador);

let contador2 = 1;
console.log(contador2++);
console.log(contador2);

let contador3 = 1;
console.log(++contador3);

let contador4 = 0;
contador4 += 2; // contador4 = contador4 + 2
// funciona para qualquer operador aritmético
// *= -= /= **=
console.log(contador4);
